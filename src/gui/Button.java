package gui;

public class Button {

	protected int x;
	protected int y;
	protected int width;
	protected int height;
	protected String name;

	public Button(int x, int y, int width, int height, String name) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.name = name;
	}

}
